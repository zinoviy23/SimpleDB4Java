package zinoviy23.simpledb4java.codegeneration;

/**
 * Interface for generated fields
 */
public interface GeneratedField {

    /**
     * Gets generated code
     * @return generated code
     */
    String getGeneratedCode();

    /**
     * Gets field name
     * @return field name
     */
    String getName();

    /**
     * Gets field type
     * @return gets field type
     */
    String getType();
}
